// Function to calculate the area of a rectangle
function rectangleArea(length, width) {
    return length * width;
  }
  
  // Function to calculate the cube of a number
  function cube(number) {
    return number ** 3;
  }
  
  // Calculate the area of a rectangle with length 5 and width 15
  console.log(`The area of the rectangle is ${rectangleArea(5, 15)}`);
  
  // Calculate the cube of numbers 5, 15, and 20
  console.log(`The cube of 5 is ${cube(5)}`);
  console.log(`The cube of 15 is ${cube(15)}`);
  console.log(`The cube of 20 is ${cube(20)}`);